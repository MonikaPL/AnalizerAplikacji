package com.example.monia.analizeraplikacji;
import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.ImageView;
import android.widget.TextView;

import java.util.List;

public class PermissionAdapter extends BaseAdapter {

    private LayoutInflater layoutInflater;
    private List<PermissionList> listStorage;

    public PermissionAdapter(Context context, List<PermissionList> customizedListView) {
        layoutInflater =(LayoutInflater)context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        listStorage = customizedListView;
    }

    @Override
    public int getCount() {
        return listStorage.size();
    }

    @Override
    public Object getItem(int position) {
        return position;
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {

        ViewHolder listViewHolder;
        if(convertView == null){
            listViewHolder = new ViewHolder();
            convertView = layoutInflater.inflate(R.layout.lista_permission, parent, false);

            listViewHolder.textInListView = (TextView)convertView.findViewById(R.id.list_permission);


            convertView.setTag(listViewHolder);
        }else{
            listViewHolder = (ViewHolder)convertView.getTag();
        }
        listViewHolder.textInListView.setText(listStorage.get(position).get_permission());


        return convertView;
    }

    static class ViewHolder{

        TextView textInListView;
    }
}


